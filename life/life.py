class Game:
    """Play the Game of Life."""

    def __init__(self, size):
        """Construct board of dimensions size x size."""
        self.board = np.zeros((size, size))

    def play(self):
        """Play the Game of Life indefinitely."""
        print("Playing life. Press ctrl + c to stop.")
        pyplot.ion()
        while True:
            self.move()
            self.show()
            pyplot.pause(0.0000005)

    def move(self):
        """Determine the states of the squares in the new step."""
        stencil = np.array([[1, 1, 1], [1, 0, 1], [1, 1, 1]])
        neighbourcount = convolve2d(self.board, stencil, mode='same')

        for i in range(self.board.shape[0]):
            for j in range(self.board.shape[1]):
                self.board[i, j] = 1 if (neighbourcount[i, j] == 3
                                         or (neighbourcount[i, j] == 2
                                             and self.board[i, j])) else 0

    def __setitem__(self, key, value):
        """Assign a board square a value."""
        self.board[key] = value

    def show(self):
        """Show board using matplotlib."""
        pyplot.clf()
        pyplot.matshow(self.board, fignum=0, cmap='binary')
        pyplot.show()

    def insert(self, pattern, s):
        """Insert a pattern centred at a given square."""
        self.board[s[0] - 1: s[0] + 2, s[1] - 1: s[1] + 2] = pattern.grid


class Pattern:
    """A Pattern that can be flipped and rotated."""

    def __init__(self, array):
        """Construct a Pattern from a numpy array of zeros and ones."""
        self.grid = array

    def flip_vertical(self):
        """Flip the pattern vertically."""
        return Pattern(self.grid[:: -1])

    def flip_horizontal(self):
        """Flip the pattern horizontally."""
        return Pattern(np.array([row[:: -1] for row in self.grid]))

    def flip_diag(self):
        """Flip the pattern diagonally."""
        return Pattern(self.grid.T)

    def rotate(self, n):
        """Rotate the pattern n x 90 degrees anticlockwise."""
        pattern = Pattern(self.grid)
        for _ in range(n % 4):
            pattern = pattern.flip_diag().flip_vertical()
        return pattern
